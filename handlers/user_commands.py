@router.message(Command('report'))
async def report_user(message: Message, bot: Bot):
    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∫–æ–º–∞–Ω–¥—É /report –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ –∂–∞–ª–æ–±—ã –Ω–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.

    –ñ–∞–ª–æ–±–∞ –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç—Å—è –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞–º —á–∞—Ç–∞. –ü—Ä–æ–≤–µ—Ä—è—é—Ç—Å—è —Å–ª–µ–¥—É—é—â–∏–µ —É—Å–ª–æ–≤–∏—è:
    - –ö–æ–º–∞–Ω–¥–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –æ—Ç–≤–µ—Ç–æ–º –Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏–µ.
    - –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –º–æ–∂–µ—Ç –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å—Å—è –Ω–∞ —Å–µ–±—è.
    - –†–µ–ø–æ—Ä—Ç –Ω–∞ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–≤ –∑–∞–ø—Ä–µ—â—ë–Ω.
    - –†–µ–ø–æ—Ä—Ç –Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –æ—Ç –≥—Ä—É–ø–ø–æ–≤—ã—Ö –∞–∫–∫–∞—É–Ω—Ç–æ–≤ (–∫–∞–Ω–∞–ª–æ–≤) —Ç–∞–∫–∂–µ –∑–∞–ø—Ä–µ—â—ë–Ω.

    Args:
        message (Message): –°–æ–æ–±—â–µ–Ω–∏–µ —Å –∫–æ–º–∞–Ω–¥–æ–π.
        bot (Bot): –û–±—ä–µ–∫—Ç –±–æ—Ç–∞ –¥–ª—è –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π.
    """
    if not message.reply_to_message:
        await message.reply("–≠—Ç–∞ –∫–æ–º–∞–Ω–¥–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –æ—Ç–≤–µ—Ç–æ–º –Ω–∞ –∫–∞–∫–æ–µ-–ª–∏–±–æ —Å–æ–æ–±—â–µ–Ω–∏–µ!")
        return

    if message.reply_to_message.from_user.id == message.from_user.id:
        await message.reply("–ù–µ–ª—å–∑—è —Ä–µ–ø–æ—Ä—Ç–∏—Ç—å —Å–∞–º–æ–≥–æ —Å–µ–±—è ü§™")
        return

    user_id = message.reply_to_message.from_user.id
    name = message.reply_to_message.from_user.username or message.reply_to_message.from_user.first_name

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–º
    admins = os.getenv('ADMIN_IDS')
    admins_list = [int(number.strip()) for number in admins.split(',')]
    if user_id in admins_list:
        await message.reply("–ê–¥–º–∏–Ω–æ–≤ —Ä–µ–ø–æ—Ä—Ç–∏—à—å? –ê–π-–∞–π-–∞–π üòà")
        return

    TELEGRAM_CHANNEL_ID = os.getenv('CHANNEL_ID')
    TELEGRAM_CHANNEL_ID = int(TELEGRAM_CHANNEL_ID)
    if message.reply_to_message.from_user.id == TELEGRAM_CHANNEL_ID:
        await message.delete()
        return

    username = message.from_user.username or message.from_user.first_name
    text = f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {username} –æ—Ç–ø—Ä–∞–≤–∏–ª —Ä–µ–ø–æ—Ä—Ç –Ω–∞ {name}!"
    chat_id = int(os.getenv('SHARECHAT_ID'))

    try:
        await bot.send_message(chat_id=chat_id, text=text)
        await message.reply(f"–†–µ–ø–æ—Ä—Ç –Ω–∞ {name} —É—Å–ø–µ—à–Ω–æ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω!")
    except Exception as e:
        print(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–µ—Ä–µ—Å—ã–ª–∫–µ —Å–æ–æ–±—â–µ–Ω–∏—è: {e}")
        await message.answer("–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –ø–µ—Ä–µ—Å—ã–ª–∫–µ —Å–æ–æ–±—â–µ–Ω–∏—è.")


