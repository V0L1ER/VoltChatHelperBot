@router.message(Command("poll"))
async def create_poll(message: Message, command: CommandObject):
    """
    –ö–æ–º–∞–Ω–¥–∞ /poll –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –æ–ø—Ä–æ—Å–∞ –≤ —á–∞—Ç–µ.

    –§–æ—Ä–º–∞—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è:
    /poll –í–æ–ø—Ä–æ—Å | –í–∞—Ä–∏–∞–Ω—Ç 1 | –í–∞—Ä–∏–∞–Ω—Ç 2 | ...

    Args:
        message (Message): –°–æ–æ–±—â–µ–Ω–∏–µ —Å –∫–æ–º–∞–Ω–¥–æ–π /poll.
        command (CommandObject): –û–±—ä–µ–∫—Ç –∫–æ–º–∞–Ω–¥—ã, —Å–æ–¥–µ—Ä–∂–∞—â–∏–π –∞—Ä–≥—É–º–µ–Ω—Ç—ã.
    """
    chat_id = message.chat.id
    member = await message.bot.get_chat_member(chat_id, message.from_user.id)

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø—Ä–∞–≤–∞ –æ—Ç–ø—Ä–∞–≤–∏—Ç–µ–ª—è
    if not isinstance(member, ChatMemberAdministrator) or not member.can_restrict_members:
        await message.reply("–£ –≤–∞—Å –Ω–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –ø—Ä–∞–≤!")
        return

    args = command.args
    if not args:
        await message.reply("–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, —É–∫–∞–∂–∏—Ç–µ –≤–æ–ø—Ä–æ—Å –∏ –≤–∞—Ä–∏–∞–Ω—Ç—ã –æ—Ç–≤–µ—Ç–∞ —á–µ—Ä–µ–∑ '|'. –ü—Ä–∏–º–µ—Ä: /poll –í–∞—à –≤–æ–ø—Ä–æ—Å | –í–∞—Ä–∏–∞–Ω—Ç 1 | –í–∞—Ä–∏–∞–Ω—Ç 2")
        return

    parts = args.split('|')
    if len(parts) < 2:
        await message.reply("–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, —É–∫–∞–∂–∏—Ç–µ —Ö–æ—Ç—è –±—ã –æ–¥–∏–Ω –≤–∞—Ä–∏–∞–Ω—Ç –æ—Ç–≤–µ—Ç–∞.")
        return

    question = parts[0].strip()
    options = [option.strip() for option in parts[1:]]

    try:
        # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ–ø—Ä–æ—Å –≤ —á–∞—Ç
        await message.answer_poll(
            question=question,
            options=options,
            is_anonymous=True  # –û–ø—Ä–æ—Å –±—É–¥–µ—Ç –∞–Ω–æ–Ω–∏–º–Ω—ã–º
        )
    except Exception as e:
        await message.reply(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –æ–ø—Ä–æ—Å–∞: {e}")
